public class Comments {

    String CommentID,CommentAuthor,Comment,CommentDate;

    public String getCommentID()
    {
        return CommentID;
    }

    public void setCommentID(String TempCommentID)
    {
        this.CommentID = TempCommentID;
    }

    public String getCommentAuthor()
    {
        return CommentAuthor;
    }

    public void setCommentAuthor(String TempCommentAuthor)
    {
        this.CommentAuthor = TempCommentAuthor;
    }

    public String getComment()
    {
        return Comment;
    }
    public void setComment(String TempComment)
    {
        this.Comment = TempComment;
    }

    public String getCommentDate()
    {
        return CommentDate;
    }
    public void setCommentDate(String tempCommentDate)
    {
        this.CommentDate = tempCommentDate;
    }



}
